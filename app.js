function openDetail(id) {
  const recipe = RECIPES.find((r) => r.id === id);
  if (!recipe) return;

  document.getElementById("detail-cat").textContent = recipe.category;
  document.getElementById("detail-name").textContent = recipe.name;
  document.getElementById("detail-stars").innerHTML = starHtml(recipe.stars);
  document.getElementById("detail-tagline").textContent = recipe.tagline || "";
  document.getElementById("detail-method").textContent = recipe.method;
  document.getElementById("detail-glass").textContent = recipe.glass || "—";
  document.getElementById("detail-garnish").textContent = recipe.garnish || "—";

  document.getElementById("detail-ingredients").innerHTML = recipe.ingredients
    .map((i) => `<li><span>${escHtml(i.name)}</span><span class="amount">${escHtml(i.amount)}</span></li>`)
    .join("");

  document.getElementById("detail-steps").innerHTML = recipe.steps
    .map((s) => `<li><span>${escHtml(s)}</span></li>`)
    .join("");

  const noteTitle = document.getElementById("note-title");
  const noteEl = document.getElementById("detail-note");
  if (recipe.note) {
    noteTitle.style.display = "";
    noteEl.textContent = recipe.note;
    noteEl.style.display = "";
  } else {
    noteTitle.style.display = "none";
    noteEl.style.display = "none";
  }

  const triviaTitle = document.getElementById("trivia-title");
  const triviaEl = document.getElementById("detail-trivia");
  const triviaText = document.getElementById("detail-trivia-text");
  if (recipe.trivia) {
    triviaTitle.style.display = "";
    triviaText.textContent = recipe.trivia;
    triviaEl.style.display = "";
  } else {
    triviaTitle.style.display = "none";
    triviaEl.style.display = "none";
  }

  document.getElementById("detail-overlay").classList.add("open");
  document.body.style.overflow = "hidden";
  /* スマホの「戻る」で閉じられるよう、擬似履歴を1つ積む */
  try { history.pushState({ rbModal: 1 }, ""); rbModalPushed = true; } catch (e) { rbModalPushed = false; }
}

function closeModalDOM() {
  document.getElementById("detail-overlay").classList.remove("open");
  document.body.style.overflow = "";
}

function closeDetail() {
  /* 擬似履歴があれば history.back() で消費（→popstateで実際に閉じる）。履歴とUIを常に同期させる */
  if (rbModalPushed) { rbModalPushed = false; try { history.back(); return; } catch (e) {} }
  closeModalDOM();
}

/* 端末の「戻る」操作 */
