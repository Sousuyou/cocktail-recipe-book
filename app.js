/* 覚えた・お気に入りの保存（localStorage・file://でも落ちないようtry-catch） */
function rbLoad(k) { try { return new Set(JSON.parse(localStorage.getItem(k) || "[]")); } catch (e) { return new Set(); } }
function rbSave(k, s) { try { localStorage.setItem(k, JSON.stringify(Array.from(s))); } catch (e) {} }
let favSet = rbLoad("rb_fav");
let learnedSet = rbLoad("rb_learned");

function toggleStatus(act, id) {
  const set = act === "fav" ? favSet : learnedSet;
  if (set.has(id)) { set.delete(id); } else { set.add(id); }
  rbSave(act === "fav" ? "rb_fav" : "rb_learned", set);
  renderRecipes();
}

function starHtml(n, cssClass) {
  return "★".repeat(n) + `<span class="dim">${"★".repeat(3 - n)}</span>`;
}

function getFilteredRecipes() {
  let list = RECIPES;
  if (statusFilter === "fav") { list = list.filter((r) => favSet.has(r.id)); }
  else if (statusFilter === "unlearned") { list = list.filter((r) => !learnedSet.has(r.id)); }
  if (currentCategory === "3" || currentCategory === "2" || currentCategory === "1") {
    list = list.filter((r) => String(r.stars) === currentCategory);
  } else if (currentCategory !== "all") {
    list = list.filter((r) => r.category === currentCategory);
  }
  if (searchQuery) {
    const q = searchQuery.toLowerCase();
    list = list.filter((r) =>
      r.name.toLowerCase().includes(q) ||
      r.tagline.toLowerCase().includes(q) ||
      r.method.toLowerCase().includes(q) ||
      r.ingredients.some((i) => i.name.toLowerCase().includes(q)) ||
      (r.note && r.note.toLowerCase().includes(q)) ||
      (r.trivia && r.trivia.toLowerCase().includes(q))
    );
  }
  return list;
}

function highlight(text) {
  if (!searchQuery) return escHtml(text);
  const q = escHtml(searchQuery).replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  return escHtml(text).replace(new RegExp(q, "gi"), (m) => `<mark>${m}</mark>`);
}

