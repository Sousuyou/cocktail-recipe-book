/* 表示モード切替（レシピ一覧 / 技法ガイド） */
function setupModeSwitch() {
  const recipeView = document.getElementById("recipe-view");
  const techniqueView = document.getElementById("technique-view");
  const countPanel = document.querySelector(".hero-panel");
  document.querySelectorAll("#mode-switch .mode-btn").forEach((btn) => {
    btn.addEventListener("click", () => {
      document.querySelectorAll("#mode-switch .mode-btn").forEach((b) => {
        b.classList.remove("active");
        b.setAttribute("aria-selected", "false");
      });
      btn.classList.add("active");
      btn.setAttribute("aria-selected", "true");
      const isTechnique = btn.dataset.mode === "technique";
      recipeView.hidden = isTechnique;
      techniqueView.hidden = !isTechnique;
      if (countPanel) countPanel.style.visibility = isTechnique ? "hidden" : "";
    });
  });
}

